const loginUser = asyncHandler(async (req, res) => {
  // req body -> data
  // check username or email
  // find the user
  // password check
  // access and refresh token we have to generate
  // send cookie
  // send response

  const { email, username, password } = req.body;

  // // if (!username || !email) {
  // if (!username && !email) {
  //   throw new ApiError(400, "username or email is required");
  // }

  // Here is an alternative of above code based on logic discussion
  if (!(username || email)) {
    throw new ApiError(400, "username or email is required");
  }

  const user = await User.findOne({
    // User -> mongoose methods available
    $or: [{ username }, { email }],
  });

  if (!user) {
    throw new ApiError(404, "User does not exist");
  }

  const isPasswordValid = await user.isPasswordCorrect(password);

  if (!isPasswordValid) {
    throw new ApiError(401, "Invalid User credentials");
  }

  const { accessToken, refreshToken } = await generateAccessAndRefreshTokens(
    user._id
  );

  // the reference of refreshToken is undefined or empty (access of RT is empty)
  const loggedInUser = await User.findById(user._id).select(
    "-password -refreshToken"
  );

  // send cookie -> now this cookie is modified by the server not from the client/frontend
  const options = {
    httpOnly: true,
    secure: true,
  };

  return res
    .status(200)
    .cookie("accessToken", accessToken, options)
    .cookie("refreshToken", refreshToken, options)
    .json(
      new ApiResponse(
        200,
        // data field (data) -> ApiResponse
        {
          user: loggedInUser,
          // why we send again separately -> accessToken & refreshToken -> Because in this user want to set in their localStorage from their side for mobile application where cookie is not set (these are the case for that we send these again)
          accessToken,
          refreshToken,
        },
        "User logged In Successfully"
      )
    );
});

